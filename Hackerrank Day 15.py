class Node:
    def __init__(self,data):
        self.data = data
        self.next = None
class Solution:
    def display(self,head):
        current = head
        while current:
            print(current.data,end=' ')
            current = current.next

    def insert(self,head,data):
        e1=Node(data)
        if head == None:
            return e1
        else:
            pointer = head
            while pointer.next != None:
                pointer=pointer.next
        pointer.next=e1
        return head




